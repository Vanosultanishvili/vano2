sum = 0  #შევქმენით ცვლადი (ჯამი) მისი მნიშვნელი 0
i = 0  #შეიქმნა საიტერაციო ცვლადი
while i <= 100: #შეიქმნა ციკლი რომლის ლიმიტი ნაკლებია ან ტოლია 100 - ის
    sum += i #ჯამს მიემატება i საიტერაციო ცვლადი
    i += 2 #იზრდება 2 - ით რათა მივიღოთ ლუწი რიცხვები

print(sum) #დაიპრინტება ჯამი





